package bgu.spl.app.gson;

/**
 * 
 * @author dotan & neta 
 * class to read file with gson
 */

public class DSObject {
	private String shoeType;
	private int amount;
	private int tick;
	
	/**
	 * 
	 * @returns shoeType
	 */
	
	public String getShoeType () {
		return shoeType;
	}
	
	/**
	 * 
	 * @return amount
	 */
	
	public int getAmount () {
		return amount;
	}
	
	/**
	 * 
	 * @return tick
	 */
	
	public int getTick () {
		return tick;
	}
}

